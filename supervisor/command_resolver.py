def is_valid_command(cmd_id: str) -> bool:
    """Check if a command ID exists in the environment (O(1))."""
    return cmd_id in _COMMANDS


def resolve_command(intent_key: str, default_label: str) -> tuple[str, str]:
    """
    Resolve an intent to the best command string for the palette.

    Returns: (command_to_type, source)
      - source is one of: "intent_map", "exact_match", "fuzzy", "default"

    Priority:
      1. Known intent → command ID mapping (INTENT_TO_COMMAND_ID)
      2. Exact match of default_label in _COMMANDS
      3. Fuzzy match against _COMMANDS_LIST
      4. Fall back to default_label (existing behaviour)
    """
    # 1. Check intent map
    if intent_key in INTENT_TO_COMMAND_ID:
        cmd_id = INTENT_TO_COMMAND_ID[intent_key]
        if is_valid_command(cmd_id):
            logger.info(
                "🗺️  Intent '%s' → '%s' (from intent map)", intent_key, cmd_id
            )
            return cmd_id, "intent_map"
        else:
            logger.warning(
                "🗺️  Intent map has '%s' → '%s' but command not in ag-commands.txt!",
                intent_key, cmd_id,
            )

    # 2. Check if default_label is itself a valid command ID
    if is_valid_command(default_label):
        logger.info(
            "🗺️  Default label '%s' is a valid command ID", default_label
        )
        return default_label, "exact_match"

    # 3. Fuzzy match (only if we have commands loaded)
    if _COMMANDS_LIST:
        # Normalize: convert human-readable to potential command ID patterns
        # e.g. "Open Chat with Agent" → try matching "antigravity.openAgent" etc.
        intent_words = intent_key.replace("_", ".").lower()

        matches = difflib.get_close_matches(
            intent_words, _COMMANDS_LIST, n=3, cutoff=0.5
        )
        if matches:
            best = matches[0]
            logger.info(
                "🗺️  Fuzzy matched '%s' → '%s' (from %d candidates)",
                intent_key, best, len(matches),
            )
            return best, "fuzzy"

        # Also try matching the default label directly
        label_lower = default_label.lower().replace(" ", ".").replace(":", "")
        matches2 = difflib.get_close_matches(
            label_lower, _COMMANDS_LIST, n=3, cutoff=0.4
        )
        if matches2:
            best = matches2[0]
            logger.info(
                "🗺️  Fuzzy matched label '%s' → '%s'", default_label, best
            )
            return best, "fuzzy"

    # 4. Fallback: return the original default label
    logger.info(
        "🗺️  No command map match for '%s' — using default: '%s'",
        intent_key, default_label,
    )
    return default_label, "default"


def find_command(query: str) -> list[str]:
    """
    Search for command IDs containing the query string (case-insensitive).
    Useful for debugging and exploration.

    Returns up to 20 matches.
    """
    query_lower = query.lower()
    results = [cmd for cmd in _COMMANDS_LIST if query_lower in cmd.lower()]
    return results[:20]


def suggest_commands(query: str, n: int = 5) -> list[str]:
    """
    Get the top-N fuzzy suggestions for a query.
    Uses difflib.get_close_matches for fuzzy matching.
    """
    if not _COMMANDS_LIST:
        return []
    return difflib.get_close_matches(query, _COMMANDS_LIST, n=n, cutoff=0.3)


def validate_and_warn(action: str) -> Optional[str]:
    """
    Validate a command palette action string.

    If it looks like a programmatic command ID (e.g. "workbench.action.xxx")
    but doesn't exist in ag-commands.txt, log a warning and return suggestions.

    If it looks like a human-readable label (contains spaces, colons), skip
    validation since those are matched by the palette's fuzzy search.

    Returns None if valid/skipped, or a warning string with suggestions.
    """
    # Human-readable labels (e.g. "Terminal: Create New Terminal") are fine
    if " " in action or ":" in action:
        return None

    # Looks like a programmatic command ID
    if is_valid_command(action):
        return None

    # Not found — suggest alternatives
    suggestions = suggest_commands(action, n=3)
    if suggestions:
        msg = (
            f"Command '{action}' not found in ag-commands.txt. "
            f"Did you mean: {', '.join(suggestions)}?"
        )
    else:
        msg = f"Command '{action}' not found in ag-commands.txt and no close matches found."

    logger.warning("🗺️  %s", msg)
    return msg


