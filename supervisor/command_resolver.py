"""
command_resolver.py — Command Map Authority for the Supervisor.

Loads the complete list of 2,483+ valid VS Code / Antigravity command IDs
from ag-commands.txt (generated by the .ag-tools/cmd-dump extension) and
provides:

  • is_valid_command(cmd_id)  — O(1) exact-match check
  • resolve_command(intent, default_label) — fuzzy match + cache
  • find_command(query) — substring search for debugging
  • suggest_commands(query, n) — top-N fuzzy suggestions

All functions are safe to call even if ag-commands.txt is missing — they
gracefully fall back to empty results so existing supervisor behaviour is
never broken.
"""

import difflib
import logging
from pathlib import Path
from typing import Optional

from . import config

logger = logging.getLogger("supervisor.command_resolver")

# ─────────────────────────────────────────────────────────────
# Command Map Loading
# ─────────────────────────────────────────────────────────────

_COMMANDS: frozenset[str] = frozenset()
_COMMANDS_LIST: list[str] = []  # Sorted list for difflib


def _load_commands() -> tuple[frozenset[str], list[str]]:
    """
    Load command IDs from ag-commands.txt.

    Skips comment lines (starting with #) and blank lines.
    Returns (frozenset for O(1) lookups, sorted list for fuzzy matching).
    """
    ag_path = config.AG_COMMANDS_PATH
    if not ag_path.exists():
        logger.warning(
            "ag-commands.txt not found at %s — command resolver disabled. "
            "Run the .ag-tools/cmd-dump extension to generate it.",
            ag_path,
        )
        return frozenset(), []

    try:
        raw = ag_path.read_text(encoding="utf-8")
        commands = []
        for line in raw.splitlines():
            line = line.strip()
            if line and not line.startswith("#"):
                commands.append(line)

        cmd_set = frozenset(commands)
        cmd_list = sorted(commands)
        logger.info(
            "✅ Loaded %d command IDs from ag-commands.txt", len(cmd_set)
        )
        return cmd_set, cmd_list

    except Exception as exc:
        logger.warning("Failed to load ag-commands.txt: %s", exc)
        return frozenset(), []


def reload_commands() -> int:
    """Force-reload the command map. Returns the number of commands loaded."""
    global _COMMANDS, _COMMANDS_LIST
    _COMMANDS, _COMMANDS_LIST = _load_commands()
    return len(_COMMANDS)


# Load on import
_COMMANDS, _COMMANDS_LIST = _load_commands()


